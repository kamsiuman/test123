
public class Printer {

    public void PrintOnce() {
        System.out.println("1111111");
    }

    public void PrintTwo() {
        System.out.println("22222");
    }

    public void PrintN(int n) {
        for (int i = 0; i < n; i++) {
            System.out.println("NNNNNNNN");
        }
    }
}